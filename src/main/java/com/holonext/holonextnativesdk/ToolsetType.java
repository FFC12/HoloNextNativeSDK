package com.holonext.holonextnativesdk;

/**
 * To specify toolset type that will be using ArView
 * Toolset can take you an advantage to initialize camera view.
 * By basic type of toolset , you can present a simple and minimal ui on camera view.
 * By advanced type of toolset , you can present more detailed ui and features that can be controlled by user
 * like colorization, image filter etc.
 */
public enum ToolsetType{
    BASIC,
    ADVANCED
}
